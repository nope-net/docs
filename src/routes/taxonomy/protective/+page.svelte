	<h1>Protective Factors</h1>

	<p class="text-xl text-gray-600 mb-8">
		Protective factors are <strong>strengths and resilience indicators</strong> that reduce risk. NOPE identifies these alongside risk factors to provide balanced assessment.
	</p>

	<h2>Why Protective Factors Matter</h2>

	<p>
		Risk assessment that only identifies problems misses half the picture. Protective factors:
	</p>

	<ul>
		<li><strong>Buffer risk</strong> — Social support reduces suicidal behavior even with ideation present</li>
		<li><strong>Guide intervention</strong> — Build on existing strengths rather than only addressing deficits</li>
		<li><strong>Improve outcomes</strong> — Protective factor enhancement is evidence-based treatment</li>
		<li><strong>Prevent over-response</strong> — High risk + strong protection ≠ same as high risk alone</li>
	</ul>

	<p>
		Our protective factors are based on the <strong>START</strong> (Short-Term Assessment of Risk and Treatability) framework.
	</p>

