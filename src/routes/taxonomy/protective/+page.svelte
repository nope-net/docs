	<h2>Clinical Framework: START</h2>

	<p>
		The <strong>Short-Term Assessment of Risk and Treatability</strong> (START) framework uniquely emphasizes:
	</p>

	<ul>
		<li><strong>Strengths alongside risks</strong> — Every risk factor has a corresponding protective factor</li>
		<li><strong>Dynamic assessment</strong> — Protective factors can change rapidly</li>
		<li><strong>Treatment planning</strong> — Build on strengths, don't just reduce risks</li>
		<li><strong>Short-term focus</strong> — What protects in the next days/weeks?</li>
	</ul>

	<p>
		NOPE applies START's balanced approach to automated assessment, ensuring protective factors are always considered alongside risk indicators.
	</p>

	<h2>Using Protective Factors</h2>

	<p>
		When integrating NOPE, consider protective factors in your response logic:
	</p>

	<pre><code>const result = await nope.evaluate(&#123; messages, config &#125;);

const hasStrongProtection = result.protective_factors_info?.protective_factor_strength === 'strong';
const isHighRisk = result.global.overall_severity === 'high' || result.global.overall_severity === 'critical';

if (isHighRisk && hasStrongProtection) &#123;
  // High risk but strong protection
  // Still show resources, but messaging can acknowledge strengths
  showResourcesWithHopeMessage(result.crisis_resources);
&#125; else if (isHighRisk && !hasStrongProtection) &#123;
  // High risk with weak protection
  // More urgent response, emphasize immediate help
  showUrgentCrisisResponse(result.crisis_resources);
&#125;</code></pre>
